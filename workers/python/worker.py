def run_worker(manifest: InputManifest) -> dict[str, Any]:
    manifest.input_frames_dir.mkdir(parents=True, exist_ok=True)
    manifest.output_frames_dir.mkdir(parents=True, exist_ok=True)

    backend_name = manifest.backend.strip().lower()
    if backend_name in {"passthrough", "classical-baseline"}:
        output_frames, backend_meta = _run_passthrough(manifest)
        mode = "passthrough"
    elif backend_name in {"realbasicvsr", "realbasicvsr-bridge"}:
        output_frames, backend_meta = _run_realbasicvsr(manifest)
        mode = "realbasicvsr"
    elif backend_name in {"seedvr-teacher", "seedvr_teacher"}:
        output_frames, backend_meta = _run_seedvr_teacher(manifest)
        mode = "seedvr-teacher"
    elif backend_name in {"stcdit-studio", "stcdit_studio"}:
        output_frames, backend_meta = _run_stcdit_studio(manifest)
        mode = "stcdit-studio"
    elif backend_name in {"detail-refiner", "detail_refiner"}:
        output_frames, backend_meta = _run_detail_refiner(manifest)
        mode = "detail-refiner"
    else:
        raise ValueError(f"unsupported worker backend `{manifest.backend}`")

    return {
        "schema_version": OUTPUT_SCHEMA_VERSION,
        "run_id": manifest.run_id,
        "clip_id": manifest.clip_id,
        "status": "ok",
        "mode": mode,
        "input_manifest_schema_version": manifest.schema_version,
        "output_frames_dir": str(manifest.output_frames_dir),
        "frame_count": len(output_frames),
        "frames": output_frames,
        "backend": backend_meta,
    }


def main() -> int:
    args = parse_args()
    manifest = load_input_manifest(args.manifest_path)
    result = run_worker(manifest)

    manifest.output_manifest_path.parent.mkdir(parents=True, exist_ok=True)
    manifest.output_manifest_path.write_text(json.dumps(result, indent=2))
    print(json.dumps(result))

    return 0


if __name__ == "__main__":
    raise SystemExit(main())
